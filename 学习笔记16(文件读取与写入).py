#所有文件分为两大类：1.文本文件 2.二进制文件（视频，表格，图片等等）
#
#如何把二进制文件读写？很简单，只需要把r,w改成rb,wb就行了

#二进制文件可以用来转移文件
#句柄=open(r'文件位置'，操作，编码转换)
#例如
fh1=open(r"C:\Users\LENOVO\Desktop\机工19-1夏坤明\作业5.jpg",'rb')
data=fh1.read()				#这一步是必须的，
							#上一步只是把文件以二进制的方式打开了，并且可以操作
							#如果不进行读出的话，没有办法进行写入
fh3=open(r'C:\Users\LENOVO\Desktop\转换文件.jpg','wb')
fh3.write(data)
fh1.close()
fh3.close()



'''附录
open有很多打开模式 以下仅供参考和查阅：
a表示append，
r表示read，
w表示write，
+表示读写模式。
b表示二进制，
t表示文本模式，
t是默认的模式。
w     以写方式打开，
a     以追加模式打开 (从 EOF 开始, 必要时创建新文件)
r+     以读写模式打开
w+     以读写模式打开
a+     以读写模式打开 
rb     以二进制读模式打开
wb     以二进制写模式打开
ab     以二进制追加模式打开 
rb+    以二进制读写模式打开 
wb+    以二进制读写模式打开 
ab+    以二进制读写模式打开 '''