#文件的写入
#文件的写入不是r，而是w
fh2=open(r"C:\Users\LENOVO\Desktop\其他\002.txt",'w')	#w=write,文件写入
	#如果文件存在，就重新写入，如果文件不不存在，就新建一个文件，并写入
