#for循环求最大值
c=[1562,564,872,6155,51564,13,946,6541]
maxx=c[0]
print(len(c))
for i in range(0,len(c)-1):  #len(c)之前提到过，是输出列表个数，减一是因为前面是从零开始，减一后循环次数才正确
	if maxx<c[i+1]:          
		maxx=c[i+1]
print(maxx)
